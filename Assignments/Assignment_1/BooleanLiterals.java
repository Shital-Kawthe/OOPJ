//7. Write a program that uses boolean literals to control program flow in an if-else statement.

public class BooleanLiterals {
    public static void main(String[] args) {
        boolean trueLiteral = true;
        boolean falseLiteral = false;

        if (trueLiteral) {
            System.out.println("True Literal is true");
        } else {
            System.out.println("True Literal is false");
        }

        if (falseLiteral) {
            System.out.println("False Literal is true");
        } else {
            System.out.println("False Literal is false");
        }
    }
}

