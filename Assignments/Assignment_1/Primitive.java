class Primitive{

int a;
float f;
char c;
byte b;
double d;
short s;
long l;
boolean b1;
public static void main(String[] args){

Primitive p=new Primitive();
System.out.println("integer default value is; " + p.a);
System.out.println("float default value is; " + p.f);
System.out.println("character default value is; " + p.c);
System.out.println("byte default value is; " + p.b);
System.out.println("double default value is; " + p.d);
System.out.println("short default value is; " + p.s);
System.out.println("long default value is; " + p.l);
System.out.println("boolean default value is; " + p.b1);
}
}

