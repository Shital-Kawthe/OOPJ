//6. Write a program to display character and string literals along with their ASCII values.

public class CharacterAndString {
    public static void main(String[] args) {
        // Character literal
        char charLiteral = 'A';
        System.out.println("Character Literal: " + charLiteral);
        System.out.println("ASCII Value: " + (int) charLiteral);

        // String literal
        String stringLiteral = "Good Morning";
        System.out.println("String Literal: " + stringLiteral);
    }
}
